
#! URL의 구조
# : URL (Uniform Resource Locator의 약자)
# : 어떤 자원의 위치를 표기하는 방법을 의미

# URL 주소 표기 형식(규격)
# 프로토콜://도메인:포트번호/경로

# URL 예시
# https://github.com/devgiants75

# 프로토콜 (https)
# : 하이퍼텍스트 문서를 전송할 때 사용하는 프로토콜(통신 규약)
# : 자원에 접근하기 위해 사용되는 규칙

# 도메인(github.com)
# : 웹 서버의 주소 (깃허브 사이트 명)

# 포트번호(생략)
# : 서비스 접근 포트 (일반적으로 생략 가능)
# : HTTP(80), HTTPS(443)

# 경로(/devgiants75)
# : 서버 내에서 자원의 정확한 위치